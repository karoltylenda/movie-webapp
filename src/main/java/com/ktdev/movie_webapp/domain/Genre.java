package com.ktdev.movie_webapp.domain;

import java.util.Set;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.ManyToMany;


@Entity
public class Genre {

    @Id
    @Column(nullable = false, updatable = false)
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(nullable = false, unique = true)
    private String name;

    @ManyToMany(mappedBy = "genreTypeGenres")
    private Set<Movie> genreTypeMovies;

    public Long getId() {
        return id;
    }

    public void setId(final Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public Set<Movie> getGenreTypeMovies() {
        return genreTypeMovies;
    }

    public void setGenreTypeMovies(final Set<Movie> genreTypeMovies) {
        this.genreTypeMovies = genreTypeMovies;
    }

}
